// @lc code=start
func maxProfit(prices []int) int {
	if len(prices) == 0 {
		return 0
	}

	var maxProfit = 0
	var minPrice = 1 << 16
	for _, price := range prices {
		if price < minPrice {
			minPrice = price
		} else if price-minPrice > maxProfit {
			maxProfit = price - minPrice
		}
	}

	return maxProfit
}

